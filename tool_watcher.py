"""
tool_watcher.py — Background analysis agent for kali-mcp-webui.

Two operating modes (set in session_meta['watch_mode']):

  CONTINUOUS (default)
  ─────────────────────
  Watches transcript.md using watchdog (instant) or polls every `poll_interval`.
  Reads only NEW lines. Triggers an LLM call when ≥ `min_new_lines` arrive.
  Streams the LLM response (if supported by provider):
    - <note>...</note> tags → pushed live as `watcher_note_token` events
    - <json>...</json> tags → parsed after for tool suggestions
  Good for: real-time live commentary and zero-latency tool spotting.

  TIMER
  ──────
  Fires a full batch analysis at a fixed `timer_interval` (seconds).
  Reads the log span requested (all / last-N-lines / last-N-minutes).
  Produces a richer, summarizing analysis note + tool suggestions.
  Good for: periodic "what's happened" snapshots over a meaningful window.
"""

